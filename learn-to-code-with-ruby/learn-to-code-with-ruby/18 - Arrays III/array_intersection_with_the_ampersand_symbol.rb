# array_intersection_with_the_ampersand_symbol
# & symbol - combine multiple arrays, keep elements that are found in both
# & - делает новый массив, сохраняя только элементы, которые есть во всех исходных (из совпадений)
p [1, 1, 2, 3, 4, 5] & [1, 4, 5, 8, 9] # [1, 4, 5]


p [1, 1, 2, 3, 4, 5].&([1, 4, 5, 8, 9]) # [1, 4, 5] в двух массивах только 1 , 4, 5 совпадают


p [1, 1, 2, 3, 4, 5] & [1, 4, 5, 8, 9] & [4, 5, 10, 11] # [4, 5] - во всех трех массивах только 4 и 5