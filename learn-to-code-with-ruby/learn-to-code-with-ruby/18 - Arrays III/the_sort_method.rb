# sort method
# метод sort

p [99, 100, 45, 11, 22].sort # сортирует числа от меньшего к большего
p [99, 100, 45, 11, 22].sort.reverse # в сочетании с reverse делает от большего к меньшему


# для string элементов по алфавиту

string_array = ["banana", "apple", "grape", "orange"]
sorted_string_array = string_array.sort

p sorted_string_array # ["apple", "banana", "grape", "orange"]
