# Они не соответствуют символам, а указывают на определенные места относительно символов в строке. Некоторые основные якори включают:

# ^ - начало строки: соответствует началу строки.
# $ - конец строки: соответствует концу строки.
# \b - граница слова: соответствует границе слова.
# Применение этих якорей в регулярных выражениях позволяет более точно определять местоположение текста в строке.