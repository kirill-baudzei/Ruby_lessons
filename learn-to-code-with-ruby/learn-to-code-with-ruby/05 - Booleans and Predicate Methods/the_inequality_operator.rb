# Inequality operator != (не равно)
puts 10 != 5  # true, так как неравно
puts 10 != 10 # false  (так как равно)

puts "Hello" != "Goodbye" # true, так как неравно
puts "Hello" != "hello" # true, так как неравно
puts "Hello" != "Hello  " # true, так как неравно
puts "Hello" != "Hello" # false (так как равно)

puts 5 != "5" # true - так как слева integer , а справа string