# insert method

catd_transactions = [ 19.99, 21, 11, 12, 49, 20.45]

catd_transactions.insert(1, 29.99) # первым элементом массива добавляет элемент 29.99
# выводит [19.99, 29.99, 21, 11, 12, 49, 20.45]
p catd_transactions 

catd_transactions.insert(0, 69.99, 100, 200) # на нулевой идекс добавляет 3 элемента.
# выводит [69.99, 100, 200, 19.99, 29.99, 21, 11, 12, 49, 20.45]
p catd_transactions

p [1, 2, 3].insert(5, 100) # 5ым элементов добавляет 100, а все до него nil, которые не указаны