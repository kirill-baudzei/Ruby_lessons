# особенности метода p в отличии от puts

puts "Steven Seagal" 
p "Steven Seagal" # сохраняет кавычки при выводе в аутпут, но и делает новую строку

puts 5
puts "5"
p 5
p "5" # так же выведет в кавычках в отличии от верхних записей 

puts "Hi there\nline break"
p "Hi there\nline break" # Выведет с кавычками и спец символом \n