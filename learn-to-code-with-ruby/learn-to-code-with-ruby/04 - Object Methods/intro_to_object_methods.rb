# введение в методы 

puts "Hello World".length # метод выводит кол-во символов в стринге - 11

puts "hello world".upcase # сделает весь string капсом. выведет HELLO WORLD

puts "TOTALLY DIFFERENT".downcase() # сделает все в lowercase - выведет totally different

# () - в конце метода нужны для придания значений методу, если нужно. Если не нужно, то они ни на что не влияют. Но для красивого кода
# их лучше убирать

expressins = "TOTALLY DIFFERENT"
puts expressins.downcase # сделает все в lowercase - выведет totally different. Аналогично записи выше





