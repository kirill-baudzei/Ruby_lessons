# quotes  - о одиночных и двойных кавычках

# можно использовать любые кавычки
puts "Hello!\nWorld" # можно использовать спец записи
puts 'Hello!\nsss' # одиночные кавычки не видят спец символов и записей внутри

phrase = "Hello world"
puts "My phrase is #{phrase}" 
puts 'My phrase is #{phrase}' # в одиночных так же не запишешь вызов переменной

puts "Hello world"